export const DoctorSlice = createSlice({
  name: 'doctorlist',
  initialState,
  reducers: {
    AddTodoPD: (state, action) => {
      state.doctorlist.push(action.payload)
    },
    DeleteTodoD:(state,action)=>{
      state.doctorlist=state.doctorlist.filter(el=>el.idD!==action.payload.idD)
    },
    DoneTodoPD:(state,action)=>{
      let i =state.doctorlist.findIndex(el=>el.idH==action.payload.idPD)
      state.doctorlist[i]={...state.doctorlist[i], isDone:!state.doctorlist[i].secteurD}
     
    },
    EditTodoD:(state,action)=>{
      let i =state.doctorlist.findIndex(el=>el.idD==action.payload.idD)
      state.doctorlist[i]={...state.doctorlist[i], descriptionD:action.payload.secteurD}
    }
  },
})

// Action creators are generated for each case reducer function
export const {AddTodoD,DeleteTodoD, DoneTodoD,EditTodoD} = DoctorSlice.actions

export default DoctorSlice.reducer